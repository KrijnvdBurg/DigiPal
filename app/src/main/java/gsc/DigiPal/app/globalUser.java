public class globalUser {
    private String localUser_UUID;
    private String localUser_username;
    private String localUser_firstName;
    private String localUser_lastName;


    public String getLocalUser_UUID() {return localUser_UUID;}
    public void setLocalUser_UUID(String localUser_UUID) {this.localUser_UUID = localUser_UUID;}

    public String getLocalUser_username() {return localUser_username;}
    public void setLocalUser_username(String localUser_username) {this.localUser_username = localUser_username;}

    public String getLocalUser_firstName() {return localUser_firstName;}
    public void setLocalUser_firstName(String localUser_firstName) {this.localUser_firstName = localUser_firstName;}

    public String getLocalUser_lastName() {return localUser_lastName;}
    public void setLocalUser_lastName(String localUser_lastName) {this.localUser_lastName = localUser_lastName;}
}
